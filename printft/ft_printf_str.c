#include "ft_printf.h"

int	printf_str(char *str)
{
	int	i;

	i = 0;
	if (!str)
	{
		if (write(1, "(null)", 6) == -1)
			return (-1);
		return (6);
	}
	while (*str != '\0')
	{
		if (write(1, str, 1) == -1)
			return (-1);
		i++;
		str++;
	}
	return (i);
}

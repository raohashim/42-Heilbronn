#include "libft.h"

int	ft_strncmp(const char *str1, const char *str2, size_t n_bytes)
{
	size_t	i;

	i = 0;
	if (!(str1 || str2))
		return ((unsigned char)str1[i] - (unsigned char)str2[i]);
	while ((i < n_bytes) && (str1[i] != '\0' || str2[i] != '\0'))
	{
		if (str1[i] != str2[i])
			return ((unsigned char)str1[i] - (unsigned char)str2[i]);
		i++;
	}
	return (0);
}
